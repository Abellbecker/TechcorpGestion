    private void mostrarDatosEnPanel(int fila) { // Mostrar datos del empleado seleccionado en el pnlDato
        if (fila == -1) return;
        String id = (String) modeloTabla.getValueAt(fila, 0); //obtener valor de tal fila
        int indice = buscarIndicePorId(id);
        if (indice != -1) {
            llenarCampos(empleados.get(indice));
        }
    }
    
    private void buscarYPosicionar(int desde, String texto) { // Buscar empleado y posicionarse
        if (texto.isEmpty()) return;
        if (desde == modeloTabla.getRowCount() - 1) {
            int resp = javax.swing.JOptionPane.showConfirmDialog(AplicacionGestionv5.this,
                "La búsqueda empezará desde el inicio.",
                "¿Está seguro?", javax.swing.JOptionPane.YES_NO_OPTION);
            if (resp == javax.swing.JOptionPane.YES_OPTION) {
                desde = -1;
            } else {
                return;
            }
        }
        desde++;
        for (int i = desde; i < modeloTabla.getRowCount(); i++) {
            for (int j = 0; j < modeloTabla.getColumnCount(); j++) {
                Object value = modeloTabla.getValueAt(i, j);
                if (value != null && value.toString().toLowerCase().contains(texto.toLowerCase())) {
                    tblListado.setRowSelectionInterval(i, i);
                    tblListado.scrollRectToVisible(tblListado.getCellRect(i, 0, true));
                    return;
                }
            }
        }
        javax.swing.JOptionPane.showMessageDialog(this, "No se encontró ningún empleado con ese texto.");
    }

    private boolean validarCampos() { // Validar campos del formulario
        if (cmbTipo.getSelectedIndex() == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un tipo.");
            return false;
        }
        if (txtId.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese un ID.");
            return false;
        }
        if (txtNombre.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese un nombre.");
            return false;
        }
        if (txtSalario.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese un salario.");
            return false;
        }
        if (txtEspecializacion.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese una especialización.");
            return false;
        }
        return true;
    }

    private int buscarIndicePorId(String id) { // Buscar índice por ID
        for (int i = 0; i < empleados.size(); i++) {
            if (empleados.get(i).getIdEmpleado().equalsIgnoreCase(id)) {
                return i;
            }
        }
        return -1;
    }

    private void mostrarResumen() { // Mostrar resumen al salir
        int cantidadDesarrollador = 0, cantidadGerente = 0, cantidadDisenador = 0, cantidadAnalista = 0;
        double sumaSalarios = 0;
        for (Empleado e : empleados) {
            sumaSalarios += e.getSalario();
            if (e instanceof Desarrollador) cantidadDesarrollador++;
            else if (e instanceof Gerente) cantidadGerente++;
            else if (e instanceof Disenador) cantidadDisenador++;
            else if (e instanceof AnalistaDatos) cantidadAnalista++;
        }
        double promedio = empleados.isEmpty() ? 0 : sumaSalarios / empleados.size();

        javax.swing.JOptionPane.showMessageDialog(this,
            "Resumen final:\n" +
            "Total empleados: " + empleados.size() + "\n" +
            "Salario promedio: $" + String.format("%.2f", promedio) + "\n" +
            "Desarrolladores: " + cantidadDesarrollador + "\n" +
            "Gerentes: " + cantidadGerente + "\n" +
            "Diseñadores: " + cantidadDisenador + "\n" +
            "Analistas: " + cantidadAnalista,
            "Resumen", javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }

