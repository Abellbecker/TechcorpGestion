        btnConfirmar.addActionListener(new java.awt.event.ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent e) {
                if (!validarCampos()) return;
                String id = "", nombre="", especial = "", tipo = "";
                double salario = 0.0;
                try {
                    id = txtId.getText().trim();
                    nombre = txtNombre.getText().trim();
                    salario = Double.parseDouble(txtSalario.getText().trim());
                    especial = txtEspecializacion.getText().trim();
                    tipo = (String) cmbTipo.getSelectedItem();
                } catch (NumberFormatException ex) {
                    javax.swing.JOptionPane.showMessageDialog(AplicacionGestionv5.this, "Salario debe ser un número válido.");
                    return;
                }
                Empleado emp = null;
                switch (tipo) {
                    case "Desarrollador":
                        emp = new Desarrollador(nombre, id, salario, especial, tipo);
                        break;
                    case "Gerente":
                        emp = new Gerente(nombre, id, salario, especial, tipo);
                        break;
                    case "Diseñador":
                        emp = new Disenador(nombre, id, salario, especial, tipo);
                        break;
                    case "Analista de Datos":
                        emp = new AnalistaDatos(nombre, id, salario, especial, tipo);
                        break;
                }
                if (emp == null) return;
                if (operacion.equals("i") && buscarIndicePorId(id) != -1) {
                    javax.swing.JOptionPane.showMessageDialog(AplicacionGestionv5.this, "Ya existe un empleado con ese ID.");
                    return;
                }
                int indice = -1;
                if (operacion.equals("i")) { // inserta
                    empleados.add(emp);
                    indice = modeloTabla.getRowCount()-1;
                    javax.swing.JOptionPane.showMessageDialog(AplicacionGestionv5.this, "Empleado agregado correctamente.");
                }
                if (operacion.equals("m")) { // modifica
                    id = (String) modeloTabla.getValueAt(tblListado.getSelectedRow(), 0);
                    indice = buscarIndicePorId(id);
                    if (indice != -1) {
                        empleados.set(indice, emp);
                        javax.swing.JOptionPane.showMessageDialog(AplicacionGestionv5.this, "Empleado modificado correctamente.");
                    }
                }
                if (operacion.equals("e")) { // elimina
                    id = (String) modeloTabla.getValueAt(tblListado.getSelectedRow(), 0);
                    indice = buscarIndicePorId(id);
                    if (indice != -1) {
                        empleados.remove(indice);
                        javax.swing.JOptionPane.showMessageDialog(AplicacionGestionv5.this, "Empleado eliminado.");
                    }
                }
                cargarTabla();
                deshabilitarEdicion();
                if (modeloTabla.getRowCount() == 0) { // Tabla vacía: nada seleccionado
                    limpiarCampos();
                } else if (indice >= modeloTabla.getRowCount()) { // se eliminó el último o se sale del rango: ir al último
                    tblListado.setRowSelectionInterval(modeloTabla.getRowCount() - 1, modeloTabla.getRowCount() - 1);
                } else { // Mantener la misma posición
                        System.out.println(indice);
                    try {
                    tblListado.setRowSelectionInterval(indice, indice);
                    } catch(Exception ex) {
                        System.out.println("x: "+ex);
                    }
                }
            }
        });

